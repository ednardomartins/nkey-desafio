package com.nkey.domain;

public class Preco {

	private String price;
	private String criteria;

	public String getPreco() {
		return price;
	}

	public void setPreco(String preco) {
		this.price = preco;
	}

	public String getNome() {
		return criteria;
	}

	public void setNome(String nome) {
		this.criteria = nome;
	}
}
